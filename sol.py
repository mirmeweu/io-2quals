    a = ''
    for i in range(len(s)):
        a += "chr(" + str(ord(s[i])) + ")"
        if i != len(s) - 1:
            a += ","
    
    # –§–æ—Ä–º–∏—Ä—É–µ–º –∫–æ–Ω–µ—á–Ω—É—é —Å—Ç—Ä–æ–∫—É –±–µ–∑ –ø—Ä–æ–±–µ–ª–æ–≤
