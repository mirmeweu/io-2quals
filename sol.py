    result = ''
    for char in a:
        if 97 <= ord(char) <= 122:  # —Å—Ç—Ä–æ—á–Ω—ã–µ a-z
            result += chr(ord(char) + 119737)
        else:
            result += char
    
    # –°–æ—Ö—Ä–∞–Ω—è–µ–º –≤ —Ñ–∞–π–ª
    with open('payload.txt', 'w', encoding='utf-8') as f:
        f.write(result)
    
